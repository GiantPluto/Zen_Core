    /// @{
public:
    typedef I_Connection*                                pScriptObject_type;
    typedef Scripting::ObjectReference<I_Connection>     ScriptObjectReference_type;
    typedef Scripting::ObjectReference<I_Connection>     ScriptWrapper_type;
