//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string&
EnvironmentHandler::getEnvironment(const std::string &_name) const
{
    Environment_type::const_iterator iter = m_environment.find(_name);

    if(iter == m_environment.end())
    {
        return m_empty;
    }

    return iter->second;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
EnvironmentHandler::setEnvironment(const std::string& _name, const std::string& _value)
{
    m_environment[_name] = _value;
}

