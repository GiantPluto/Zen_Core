//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
EnvironmentHandler::appendEnvironment(const Environment_type& _environment)
{
    for(Environment_type::const_iterator iter = _environment.begin(); iter != _environment.end(); iter++)
    {
        m_environment[iter->first] = iter->second;
    }
}

