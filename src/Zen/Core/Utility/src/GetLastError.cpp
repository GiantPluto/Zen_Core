#include "../GetLastError.hpp"

#ifdef HOST_WIN32
#include <Windows.h>
#elif defined(HOST_POSIX) || defined(__APPLE__)
#include <errno.h>
#endif
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
