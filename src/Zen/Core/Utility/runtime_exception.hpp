#ifndef ZEN_UTILITY_RUNTIME_EXCEPTION_HPP_INCLUDED
#define ZEN_UTILITY_RUNTIME_EXCEPTION_HPP_INCLUDED

#include <string>
#include <stdexcept>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Zen {
namespace Utility {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
;

class runtime_exception
:   public std::exception
{
    /// @name runtime_exception implementation
    /// @{
public:
    virtual const char* what() const throw();
    /// @}

    /// @name 'Structors
    /// @{
public:
             runtime_exception(const std::string& _msg);
    virtual ~runtime_exception() throw();
    /// @}

    /// @name Member Variables
    /// @{
protected:
    std::string m_msg;
    /// @}

};	// runtime_exception


//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
// Inlines
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
inline
runtime_exception::runtime_exception(const std::string& _msg)
:   m_msg(_msg)
{    
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
inline
runtime_exception::~runtime_exception() throw()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
inline
const char* runtime_exception::what() const throw ()
{
    return m_msg.c_str();
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Utility
}   // namespace Zen
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

#endif // ZEN_UTILITY_RUNTIME_EXCEPTION_INCLUDED
