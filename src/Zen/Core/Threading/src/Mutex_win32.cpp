#ifdef THREADMODEL_WIN32

#include "Mutex_win32.hpp"

#include <stdexcept>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Zen {
namespace Threading {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

Mutex_win32::Mutex_win32()
{
    m_handle = ::CreateMutex(NULL, FALSE, NULL);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Mutex_win32::~Mutex_win32()
{
    if (m_handle != NULL)
    {
        ::CloseHandle(m_handle);
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Mutex_win32::acquire()
{
    if (::WaitForSingleObject(m_handle, INFINITE) != WAIT_OBJECT_0)
    {
        throw std::runtime_error("Mutex_win32::acquire(): lock acquisition failed.");
    }
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Mutex_win32::release()
{
    if (::ReleaseMutex(m_handle) == 0)
    {
        throw std::runtime_error("Mutex_win32::release(): lock release failed.");
    }
}


//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Threading
}   // namespace Zen
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

#endif // THREADMODEL_WIN32
