#ifndef ZEN_THREADING_I_RUNNABLE_HPP_INCLUDED
#define ZEN_THREADING_I_RUNNABLE_HPP_INCLUDED

#include "Configuration.hpp"

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Zen {
namespace Threading {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

class I_Runnable
{
    /// @name Types
    /// @{
public:
    /// @}

    /// @name I_Runnable interface
    /// @{
public:
	/// Run to completion or until stop() is called.
	virtual void run() throw() = 0;

	/// When this method is called, the I_Runnable implementation
	/// should exit gracefully from the run() method.
	virtual void stop() {}
    /// @}

    /// @name 'Structors
    /// @{
protected:
			 I_Runnable() {}
public:
	virtual ~I_Runnable() {}
    /// @}

};	// interface I_Runnable

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Threading
}   // namespace Zen
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

#endif // ZEN_THREADING_I_RUNNABLE_HPP_INCLUDED
