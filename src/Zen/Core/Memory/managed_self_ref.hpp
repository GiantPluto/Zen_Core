#ifndef ZEN_MEMORY_MANAGED_SELF_REFERENCE_HPP_INCLUDED
#define ZEN_MEMORY_MANAGED_SELF_REFERENCE_HPP_INCLUDED

#include <Zen/Core/Memory/managed_weak_ptr.hpp>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Zen {
namespace Memory {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
;
template<typename element_type>
class managed_self_ref
{
    /// @name Types
    /// @{
public:
    typedef managed_weak_ptr<element_type>      pWeak_type;
    /// @}

    /// @name managed_self_ref implementation
    /// @{
public:
    void setSelfReference(pWeak_type _pSelf);
    pWeak_type& getSelfReference();
    /// @}

    /// @name 'Structors
    /// @{
protected:
             managed_self_ref();
    virtual ~managed_self_ref();
    /// @}

    /// @name Member Variables
    /// @{
private:
    pWeak_type  m_pSelfReference;
    /// @}

};  // template class managed_self_ref

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
template<typename element_type>
inline
managed_self_ref<element_type>::managed_self_ref()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
template<typename element_type>
inline
managed_self_ref<element_type>::~managed_self_ref()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
template<typename element_type>
inline
void
managed_self_ref<element_type>::setSelfReference(pWeak_type _pSelf)
{
    m_pSelfReference = _pSelf;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
template<typename element_type>
inline
typename managed_self_ref<element_type>::pWeak_type&
managed_self_ref<element_type>::getSelfReference()
{
    return m_pSelfReference;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Memory
}   // namespace Zen
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

#endif // ZEN_MEMORY_MANAGED_SELF_REFERENCE_HPP_INCLUDED
